// Standard Wiimote (RVL-CNT-01) HID report descriptor. Factual protocol
// data reconstructed from WiiBrew (https://wiibrew.org/wiki/Wiimote) —
// this is what the real Wiimote advertises over L2CAP after SDP setup.
//
// Layout mirrors the official descriptor: every output (host -> Wiimote)
// report ID plus every input (Wiimote -> host) report ID is declared.
// Report sizes match the WiiBrew report format table.

#ifndef WIIMOTE_HID_DESCRIPTOR_H
#define WIIMOTE_HID_DESCRIPTOR_H

#include <stdint.h>

// Each report entry below follows the same HID template:
//   0x85, <id>          // Report ID
//   0x95, <size>        // Report Count (bytes)
//   0x75, 0x08          // Report Size (8 bits)
//   0x15, 0x00          // Logical Minimum (0)
//   0x26, 0xff, 0x00    // Logical Maximum (255)
//   0x09, 0x01          // Usage (Vendor-defined)
//   0x8[12], 0x02       // Output (0x91) / Input (0x81), Data, Var, Abs
//
// We use Generic Desktop / Joystick usage page since the Wii sees it as a
// peripheral, but most of the actual data is vendor-defined.

static const uint8_t wiimote_hid_descriptor[] = {
    0x05, 0x01,             // Usage Page (Generic Desktop)
    0x09, 0x05,             // Usage (Gamepad)
    0xA1, 0x01,             // Collection (Application)

    // ----- OUTPUT REPORTS (host -> Wiimote) -----

    // 0x11 LEDs (1 byte)
    0x85, 0x11, 0x15, 0x00, 0x26, 0xff, 0x00, 0x75, 0x08, 0x95, 0x01, 0x09, 0x01, 0x91, 0x00,
    // 0x12 Data reporting mode (2 bytes)
    0x85, 0x12, 0x95, 0x02, 0x09, 0x01, 0x91, 0x00,
    // 0x13 IR camera enable (1 byte)
    0x85, 0x13, 0x95, 0x01, 0x09, 0x01, 0x91, 0x00,
    // 0x14 Speaker enable (1 byte)
    0x85, 0x14, 0x95, 0x01, 0x09, 0x01, 0x91, 0x00,
    // 0x15 Status request (1 byte)
    0x85, 0x15, 0x95, 0x01, 0x09, 0x01, 0x91, 0x00,
    // 0x16 Write memory (21 bytes)
    0x85, 0x16, 0x95, 0x15, 0x09, 0x01, 0x91, 0x00,
    // 0x17 Read memory (6 bytes)
    0x85, 0x17, 0x95, 0x06, 0x09, 0x01, 0x91, 0x00,
    // 0x18 Speaker data (21 bytes)
    0x85, 0x18, 0x95, 0x15, 0x09, 0x01, 0x91, 0x00,
    // 0x19 Speaker mute (1 byte)
    0x85, 0x19, 0x95, 0x01, 0x09, 0x01, 0x91, 0x00,
    // 0x1a IR camera enable 2 (1 byte)
    0x85, 0x1a, 0x95, 0x01, 0x09, 0x01, 0x91, 0x00,

    // ----- INPUT REPORTS (Wiimote -> host) -----

    // 0x20 Status (6 bytes)
    0x85, 0x20, 0x95, 0x06, 0x09, 0x01, 0x81, 0x02,
    // 0x21 Read-memory response (21 bytes)
    0x85, 0x21, 0x95, 0x15, 0x09, 0x01, 0x81, 0x02,
    // 0x22 Write-memory ack (4 bytes)
    0x85, 0x22, 0x95, 0x04, 0x09, 0x01, 0x81, 0x02,
    // 0x30 Core buttons (2 bytes)
    0x85, 0x30, 0x95, 0x02, 0x09, 0x01, 0x81, 0x02,
    // 0x31 Core + accel (5 bytes)
    0x85, 0x31, 0x95, 0x05, 0x09, 0x01, 0x81, 0x02,
    // 0x32 Core + 8 ext (10 bytes)
    0x85, 0x32, 0x95, 0x0a, 0x09, 0x01, 0x81, 0x02,
    // 0x33 Core + accel + 12 IR (17 bytes)
    0x85, 0x33, 0x95, 0x11, 0x09, 0x01, 0x81, 0x02,
    // 0x34 Core + 19 ext (21 bytes)
    0x85, 0x34, 0x95, 0x15, 0x09, 0x01, 0x81, 0x02,
    // 0x35 Core + accel + 16 ext (21 bytes)
    0x85, 0x35, 0x95, 0x15, 0x09, 0x01, 0x81, 0x02,
    // 0x36 Core + 10 IR + 9 ext (21 bytes)
    0x85, 0x36, 0x95, 0x15, 0x09, 0x01, 0x81, 0x02,
    // 0x37 Core + accel + 10 IR + 6 ext (21 bytes)
    0x85, 0x37, 0x95, 0x15, 0x09, 0x01, 0x81, 0x02,
    // 0x3d 21 ext (21 bytes)
    0x85, 0x3d, 0x95, 0x15, 0x09, 0x01, 0x81, 0x02,
    // 0x3e / 0x3f Interleaved (21 bytes each)
    0x85, 0x3e, 0x95, 0x15, 0x09, 0x01, 0x81, 0x02,
    0x85, 0x3f, 0x95, 0x15, 0x09, 0x01, 0x81, 0x02,

    0xC0                    // End Collection
};

#define WIIMOTE_HID_DESCRIPTOR_SIZE (sizeof(wiimote_hid_descriptor))

#endif // WIIMOTE_HID_DESCRIPTOR_H
